use std::fmt::Debug;

use super::*;

#[derive(PartialEq)]
struct HexAdapter<'a>(&'a [u8]);

impl<'a> Debug for HexAdapter<'a> {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "{:02X?}", self.0.as_ref())
    }
}

#[test]
fn request_packet() {
    let frame = Frame {
        local_seq: 0x54,
        remote_seq: 0,
        serial: 3185936016,
        packet: RequestPacket {
            modbus_payload: Box::new([0x01, 0x03, 0x00, 0x00, 0x00, 0x01, 0x84, 0x0a]),
        },
    };

    let expected_bytes = [
        0xa5, 0x17, 0x00, 0x10, 0x45, 0x54, 0x00, 0x90, 0x86, 0xe5, 0xbd, 0x02, 0x00, 0x00, 0x00,
        0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x01, 0x03, 0x00, 0x00,
        0x00, 0x01, 0x84, 0x0a, 0x0d, 0x15,
    ];

    assert_eq!(
        HexAdapter(&frame.encode_to_vec()),
        HexAdapter(&expected_bytes)
    )
}

#[test]
fn parse_complete_packet() {
    let bytes = [
        0xa5, 0x15, 0x00, 0x10, 0x15, 0xeb, 0xb7, 0x90, 0x86, 0xe5, 0xbd, 0x02, 0x01, 0x2b, 0x15,
        0x04, 0x00, 0x6e, 0x21, 0x00, 0x00, 0x00, 0xa7, 0xae, 0x69, 0x01, 0x03, 0x02, 0x00, 0x05,
        0x78, 0x47, 0xf2, 0x15,
    ];

    let expected_frame = Frame {
        local_seq: 0xeb,
        remote_seq: 0xb7,
        serial: 3185936016,
        packet: ParsedPacket::Response(ResponsePacket {
            frame_type: 0x02,
            status: 0x01,
            total_working_time: 267563,
            power_on_time: 8558,
            offset_time: 1773053696,
            modbus_payload: Box::from([0x01, 0x03, 0x02, 0x00, 0x05, 0x78, 0x47]),
        }),
    };

    assert_eq!(
        parse_frame(&bytes).unwrap(),
        Some((expected_frame, bytes.len()))
    );
}
